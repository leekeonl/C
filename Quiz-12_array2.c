/*
가로의 길이가 9이고, 세로의 길이가 3인 배열을 선언하세요..
=> int  arr[3][9];

   위 배열의 각 행에 구구단의 결과값을 순서대로  입력하세요.
   배열안의 전체  데이터를 출력합니다.
   단, 각단의 열단위 값을 세로로 더해서 출력하세요..
   사용 할 변수 : i , j , res
  
   2   4   6   8  10  12  14  16  18
   3   6   9  12  15  18  21  24  27
   4   8  12  16  20  24  28  32  36
   ---------------------------------
   9  18  27  36  45  54  63  72  81
*/
int main() {
	int ar[3][9];
	int i, j, res;

	// 입력
	for (i = 0; i < 3; i++) {
		for (j = 0; j < 9; j++) {
			ar[i][j] = (i + 2)*(j + 1);
			printf("%2d ", ar[i][j]);
		}
		printf("\n");
	}
	printf("-----------------------------\n");
	// 출력
	for (i = 0; i < 9; i++) {
		res = 0;
		for (j = 0; j < 3; j++) {
			res += ar[j][i];
		}
		printf("%2d ", res);
	}
	printf("\n");

}